class Pet:
    def __init__(self, name, species, date_of_birth, owner, vet, treatment_notes, checked_in = False, id = None):
        self.name = name
        self.species = species
        self.date_of_birth = date_of_birth
        self.owner = owner
        self.vet = vet
        self.treatment_notes = treatment_notes
        self.checked_in = checked_in
        self.id = id



